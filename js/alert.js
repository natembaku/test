// Weather conditions

let weather = prompt("What's the weather like?");
if (weather === "sunny") {
    alert("T-shirt, shorts and flip flops time!");
} else if (weather === "windy") {
    alert("Windbreaker life.");
} else if (weather === "rainy") {
    alert("Bring that umbrella cute-py!");
} else if (weather === "snowy") {
    alert("Just stay inside and be a couch potato, hot chocolate and Netflix!");
} else {
    alert("Not a valid weather type");
}